<h3>2025.04.01. Bardócz Attila (Ata): Etiópia ősi földje</h3>

<p>Az a megtiszteltetés ért minket, hogy Ata nálunk mutatta be először az Etióp útjairól szóló előadását.
</p>

<p>
Nagyon érdekes dolgokat mondott el nekünk:
</p>

<p>
- átmentek egy sósivatagon, ami 40 - 50 méter mélységben tartalmaz sót
</p>

<p>
- ezt a kínaiak már el is kezdték megszerezni
</p>

<p>
- egy kráternél olyan láván lépkedett, ami még meleg volt
</p>

<p>
- jártak egy hőforrásnál, ahol kénsavas tavak keletkeztek
</p>

<p>
- van olyan törzs, ahol darált banánhéjból készítenek lepényt
</p>

<p>
- egy másik törzsnél nincsenek temetők, a halottakat ott temetik el, ahol elhunytak, semmi sem jelöli a sírjaikat
</p>

<p>
- a kávé Kaffa tartományból származik, de az etiópok bunna - nak hívják ezt az italt
